public class SampleSwitch {
    public void switchSampleMethod(){
        for (int i =0; i<8; i++ ){
            switch (i)
            {
                case 0:
                    System.out.println("i : " + i);
                    break;
                case 1:
                    System.out.println("i : " + i);
                    break;
                case 2:
                    System.out.println("i : " + i);
                    break;
                case 3:
                    System.out.println("i : " + i);
                    break;
                case 4:
                    System.out.println("i : " + i);
                    break;
                default:
                    System.out.println("i is greater than 4 ");
                    break;
            }
        }
    }
}
